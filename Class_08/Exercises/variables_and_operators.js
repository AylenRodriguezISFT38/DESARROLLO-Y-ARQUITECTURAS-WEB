// Crear dos variables numéricas y utilizar el operador suma para guardar el valor
// de la suma de ambos números en una 3er variable
var num1 = 5;
var num2 = 10;
var suma = num1 + num2;
console.log("La suma de", num1, "y", num2, "es: ", suma);

// Crear dos variables de tipo String y concatenarlas guardando el resultado en una
// 3er variable.
var bienvenida = "Bienvenidos a javascript";
var materia = "- Desarrollo y arquitectura web";
var concat = bienvenida + " " + materia;
console.log("La concatenacion de las dos variables es: ", concat);

// Crear dos variables de tipo String y sumar el largo de cada variable (cantidad de
// letras del string) guardando el resultado de la suma en una 3er variable (utilizar
// length).
var str1 = "Hello";
var str2 = "World";
var total = str1.length + str2.length;
console.log("La suma del largo de", str1, "y", str2, "es: ", total);